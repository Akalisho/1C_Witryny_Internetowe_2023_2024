let zmienna;

zmienna = prompt("Podaj liczbę pierwszą: ");

let zmienna2;

zmienna2 = prompt("Podaj liczbę drugą: ");
document.write("Dodawanie: ");
document.write(zmienna + zmienna2);
document.write("Odejmowanie: ");
document.write(zmienna - zmienna2);
document.write("Mnożenie: ");
document.write(zmienna * zmienna2);
document.write("Dzielenie: ");
document.write(zmienna / zmienna2);